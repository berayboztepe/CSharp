using System;

namespace DortIslem
{
    public class MatematikselIslemler
    {
        public static int Topla(int sayi1, int sayi2)
        {
            return sayi1 + sayi2;
        }

        public int Cikar(int sayi1, int sayi2)
        {
            return sayi1 - sayi2;
        }
    }
}
